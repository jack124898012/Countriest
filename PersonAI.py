class Person:
    def __init__(self, loc):
        self.fails = 0
        self.loc = loc
        self.locations = []
        self.remove = False
    
    def move(location):
        if remove: return
        self.locations.append(location)
        self.loc = location

    def fail():
        self.fails += 1
        if fails == 10: self.destroy()

    def destroy():
        self.remove = True
